#Texto de prueba

texto= "Este es el texto de prueba"
resultado = texto
print(resultado)

#Metodo Upper convertir en mayusculas
resultado = texto.upper()
print(resultado)

#Metodo Lowe convertir en minusculas
resultado = texto.lower()
print(resultado)

#Metodo Split separar
resultado = texto.split()
print(resultado)


#Variables que se usaran
x = 6
y = 2
z = 7

#operadores
print(f"{x} mas {y} es igual a {x+y}")
print(f"{x} menos {y} es igual a {x-y}")
print(f"{x} por {y} es igual a {x*y}")
print(f"{x} entre {y} es igual a {x/y}")

#hacemos una divisi√≥n al piso
# si tenemos 7/2 = 3.5 pero solo nos quedamos con 3
print(f"{z} dividido al piso de {y} es igual a {z//y}")

#hacemos una division con modulo
# si tenemos 7/2, caben 3 veces pero sobra 1, es el modulo
print(f"{z} modulo de {y} es igual {z%y}")

#elevamos al cubo
print(f"{x} elevado {y} es igual a {x**y}")

#raiz cuadrada
print(f"la raiz cuadrada de {x} es {x**0.5}")